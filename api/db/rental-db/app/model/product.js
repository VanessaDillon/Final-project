class Product {
    constructor(id, name, description, price, img, color, seats, year) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.img = img;
        this.color = color;
        this.seats = seats;
        this.year = year;
        
    }
}

module.exports = Product;